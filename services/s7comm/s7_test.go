func TestBufferSerialzer(t *testing.T){
	var eh errHandler
	var T TPKT
	T.Version = 0x01
	T.Reserved = 0x02
	T.Length = 0x03


	buf := &bytes.Buffer{}

	eh.serializer(buf, T)

	if  reflect.DeepEqual(buf, []byte{0x00,0x01,0x02}){
		t.Errorf("Serialzer returned %v, but should have been %s", buf.Bytes(), "[1 2 0 3]" )
	}



	type Error struct {
		err []byte
	}
	buf = &bytes.Buffer{}
	var E Error
	E.err = []byte{0x02,0x02,0x02}
	eh.serializer(buf, E)

	if buf.Bytes() != nil{
		t.Errorf("Serializer returned %v, but an empty output was expected", buf.Bytes())
	}

}
