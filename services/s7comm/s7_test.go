func TestS7Connect(t *testing.T){
	var P Packet
	var S7 S7Packet
	P.S7.Parameter.SetupCom.Function = 0xf0
	P.S7.Parameter.SetupCom.Reserved = 0x00
	P.S7.Parameter.SetupCom.MaxAmQCalling = 0x01
	P.S7.Parameter.SetupCom.MaxAmQCalled = 0x01
	P.S7.Header.Reserved = 0x0000
	P.S7.Header.PDURef = 512
	P.S7.Header.ParamLength = 8
	P.S7.Header.DataLength = 0

	response := S7.connect(P)
	exResp := []byte{ 0x03, 0x00, 0x00, 0x1b, 0x02, 0xf0, 0x80, 0x32, 0x03, 0x00, 0x00, 0x02, 0x00, 0x00, 0x08, 0x00, 0x00, 0x00, 0x00, 0xf0, 0x00, 0x00, 0x01, 0x00, 0x01, 0x00, 0xf0}
	if !reflect.DeepEqual(response, exResp){
		t.Errorf("S7 Job request broken. \n expected: \t %x\n got: \t\t %x\n", exResp, response)
	}
}

